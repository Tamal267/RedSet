package com.example.RedSet.Ranking;

public class IndividualInformation {
    private String dept, level, name, penalty, rank, totalSolve;

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPenalty() {
        return penalty;
    }

    public void setPenalty(String penalty) {
        this.penalty = penalty;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getTotalSolve() {
        return totalSolve;
    }

    public void setTotalSolve(String totalSolve) {
        this.totalSolve = totalSolve;
    }
}
